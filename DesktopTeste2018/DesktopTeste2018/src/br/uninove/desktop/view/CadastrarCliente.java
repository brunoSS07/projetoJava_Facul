    private void btnMostrarCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarCliActionPerformed
        List<Cliente> listaCliente = new ArrayList<Cliente>();
        int cliID = Integer.parseInt(txtAltClienteID.getText());
        
        listaCliente = cDao.listarClienteID(cliID);
        
        
        for(int indice = 0; indice < listaCliente.size(); indice++){
            Cliente cliente = (Cliente) listaCliente.get(indice);
            
            txtNomeCompleto.setText(cliente.getNome());
            txtDtNasc.setText(cliente.getData_nasc());
            txtTel.setText(cliente.getTelefone());
            txtCPF.setText(cliente.getCpf());
            txtEmail.setText(cliente.getEmail());
            txtRua.setText(cliente.getEndereco().getRua());
            txtNum.setText(Integer.toString(cliente.getEndereco().getNumero()));
            txtBairro.setText(cliente.getEndereco().getBairro());
            txtCidade.setText(cliente.getEndereco().getCidade());
            txtEstado.setText(cliente.getEndereco().getEstado());
            txtCep.setText(Integer.toString(cliente.getEndereco().getCep()));
        }
            
    }//GEN-LAST:event_btnMostrarCliActionPerformed

