    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Atribuindo os valore em uma variavel
        String nome = txtNomeCompleto.getText();
        String tel = txtTel.getText();
        String email = txtEmail.getText();
        String cpf = txtCPF.getText();
        String dTNasc = txtDtNasc.getText();
        
        //Passando valores atraves do construtor com 5 parametros
        cli = new Cliente(nome, tel, email,cpf,dTNasc);
        
        //Criando um objeto do tipo Endereco para setar os valores
        Endereco end = new Endereco();
        
        //Setando os valores para a classe Endereco
        end.setRua(txtRua.getText());
        end.setNumero(Integer.parseInt(txtNum.getText()));
        end.setBairro(txtBairro.getText());
        end.setCidade(txtCidade.getText());
        end.setEstado(txtEstado.getText());
        end.setCep(Integer.parseInt(txtCep.getText()));
       
        cli.setEndereco(end);
        
        //passando os dados para o metodo inserirCliente
        cDao.inserirCliente(cli);
        
    }//GEN-LAST:event_jButton1ActionPerformed

